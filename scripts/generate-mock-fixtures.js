// CI 전용 mock fixture 생성기
// - 30개 결정적 가짜 GitHubRepo + README + 0~3 Release 를 src/test-fixtures/mock-repos.ts 로 출력
// - 사용자 로컬 fixture 가 이미 있으면(.gitignore 로 커밋 제외된 사용자 데이터) 건드리지 않음
// - ci.yml 에서 npm ci 직후 호출되며, 이어서 MOCK_REPOS=1 npm run build 로 30개 시나리오 회귀 검증
//
// ensure-mock-stub.js 와의 관계:
// - ensure-mock-stub.js: prebuild 훅. fixture 가 없으면 빈 stub(makeMockRepos=[] 등) 생성
// - generate-mock-fixtures.js (본 파일): CI 에서 build 직전에 호출. 30개 데이터로 fixture 작성
// 두 스크립트 모두 existsSync 가드를 사용하므로 사용자 로컬 fixture 가 우선

import { existsSync, mkdirSync, writeFileSync } from 'node:fs';
import { dirname } from 'node:path';

const filePath = 'src/test-fixtures/mock-repos.ts';

if (existsSync(filePath)) {
  console.log('[generate-mock] 기존 fixture 가 있어 건너뜁니다: ' + filePath);
  process.exit(0);
}

mkdirSync(dirname(filePath), { recursive: true });

